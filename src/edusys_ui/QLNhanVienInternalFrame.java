    private void fillToTable() {

        tableModel.setRowCount(0);
        for (NhanVien nhanVien : nhanVienList) {
            tableModel.addRow(new Object[]{
                nhanVien.getMaNV(), nhanVien.getMatKhau(), nhanVien.getHoTen(), nhanVien.isVaiTro() ? "Trưởng phòng" : "Nhân viên"
            });
        }
    }

    private void fillToForm(int i) {
        txtMa.setText(nhanVienList.get(i).getMaNV());
        txtTen.setText(nhanVienList.get(i).getHoTen());
        txtMK.setText(nhanVienList.get(i).getMatKhau());
        txtXN.setText(nhanVienList.get(i).getMatKhau());
        if (nhanVienList.get(i).isVaiTro()) {
            rdoTP.setSelected(true);
        } else {
            rdoNV.setSelected(true);
        }
        tblBang.setRowSelectionInterval(i, i);
        lblRecord.setText((index + 1) + " / " + (nhanVienList.size()));
    }


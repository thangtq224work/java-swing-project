    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:

        NhanVien nhanVien = getForm();
        index = tblBang.getSelectedRow();
        if (nhanVien != null) {
            if (nhanVien.getMaNV().equalsIgnoreCase(Auth.nv.getMaNV())) {
                JOptionPane.showMessageDialog(this, "Không thể xóa chính mình .", "Thông báo", JOptionPane.WARNING_MESSAGE);

            } else {
                int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa nhân viên " + txtMa.getText(), "Thông báo", JOptionPane.YES_NO_OPTION);
                if (option == JOptionPane.YES_OPTION) {
                    nhanVien.setTrangThai(true);
                    int isSuccess = daonv.updateToDB(nhanVien);
                    if (isSuccess > 0) {
                        JOptionPane.showMessageDialog(this, "Xóa nhân viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        nhanVienList.remove(index);
                        tableModel.removeRow(index);
                        if (tableModel.getRowCount() == index) {
                            index--;
                            fillToForm(index);
                        }
                        fillToForm(index);
                    } else {
                        JOptionPane.showMessageDialog(this, "Xóa nhân viên thất bại . Vui lòng xem lại dự liệu .", "Thông báo", JOptionPane.WARNING_MESSAGE);
                    }
                }
            }
        }

    }//GEN-LAST:event_btnXoaActionPerformed

    private void cbXemItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbXemItemStateChanged
        // TODO add your handling code here:
        if (cbXem.getSelectedIndex() == 0) {
            enableComponent(btnSua, btnMoi, btnXoa);
            enableComponent(btn1, btn2, btn3, btn4);
            nhanVienList.clear();
            nhanVienList = daonv.getAll();
            fillToTable();
            fillToForm(index);
        } else {
            nhanVienList.clear();
            nhanVienList = new DAONhanVien().getNAll();
            disableComponent(btnSua, btnMoi, btnXoa);
            fillToTable();
            if (nhanVienList.size() > 0) {
                fillToForm(0);
            } else {
                disableComponent(btn1, btn2, btn3, btn4);
                clearForm();
            }
        }
    }//GEN-LAST:event_cbXemItemStateChanged


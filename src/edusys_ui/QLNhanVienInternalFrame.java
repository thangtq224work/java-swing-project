    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        NhanVien nhanVien = getForm();
        if (nhanVien != null) {
            int isSuccess = daonv.insertToDB(nhanVien);
            if (isSuccess > 0) {
                JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                nhanVienList.add(nhanVien);
                tableModel.addRow(new Object[]{
                    nhanVien.getMaNV(), nhanVien.getMatKhau(), nhanVien.getHoTen(), nhanVien.isVaiTro() ? "Trưởng phòng" : "Nhân viên"
                });
                index = tableModel.getRowCount() - 1;
                btnMoi.setText("Mới");
                System.out.println(index);
                disableComponent(btnThem);
                enableComponent(btn1, btn2, btn3, btn4, btnXoa, btnSua);
                txtMa.setEnabled(false);
                specifyBtnMoi = true;
                fillToForm(index);
            } else {
                JOptionPane.showMessageDialog(this, "Thêm nhân viên thất bại", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed


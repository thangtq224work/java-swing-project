    private void fillToTable() {
        chuyenDeList = daoCD.getAll();
        tableModel.setRowCount(0);
        for (ChuyenDe cd : chuyenDeList) {
            tableModel.addRow(new Object[]{
                cd.getMaCD(), cd.getTenCD(), cd.getHocPhi(), cd.getThoiLuong(), cd.getHinh()
            });
        }
    }

    private void fillToForm(int i) {
        txtMaCD.setText(chuyenDeList.get(i).getMaCD());
        txtTenCD.setText(chuyenDeList.get(i).getTenCD());
        txtHocPhi.setText(chuyenDeList.get(i).getHocPhi() + "");
        txtThoiLuong.setText(chuyenDeList.get(i).getThoiLuong() + "");
        txtMoTa.setText(chuyenDeList.get(i).getMoTa());
//        if(chuyenDeList.get(i).getHinh()== null){
//                        System.out.println("1");
//
//        }
        if (!chuyenDeList.get(i).getHinh().trim().equals("")) {
            lblAnh.setIcon(scaleImage(XImage.read(chuyenDeList.get(i).getHinh())));
            lblAnh.setToolTipText(chuyenDeList.get(i).getHinh());
            lblAnh.setText("");
        } else {
            lblAnh.setText("Chọn ảnh");
            lblAnh.setIcon(null);
            lblAnh.setToolTipText(null);
            System.out.println("1" + lblAnh.getToolTipText());
        }
        tblBang.setRowSelectionInterval(i, i);
        lblRecord.setText((i + 1) + " / " + (chuyenDeList.size()));
    }

    private void clearForm() {
        txtMaCD.setText("");
        txtTenCD.setText("");
        txtHocPhi.setText("");
        txtThoiLuong.setText("");
        lblRecord.setText("? / " + (chuyenDeList.size()));
        lblAnh.setText("Chọn ảnh");
        lblAnh.setToolTipText(null);
        lblAnh.setIcon(null);
        tblBang.getSelectionModel().clearSelection();
    }

    private ChuyenDe getForm() {
        if (check()) {
            String ma = txtMaCD.getText().trim(), ten = txtTenCD.getText().trim(), hocPhi = txtHocPhi.getText().trim(), thoiLuong = txtThoiLuong.getText().trim(),
                    moTa = txtMoTa.getText().trim(), hinh = lblAnh.getToolTipText();
            if (hinh == null) {
                hinh = "";
            }
            int tl = Helper.parseInt(thoiLuong);
            double hp = Helper.parseDouble(hocPhi);
            return new ChuyenDe(ma, ten, hinh, moTa, (float) hp, tl);
        }
        return null;
    }

    private boolean check() {
        String ma = txtMaCD.getText().trim(), ten = txtTenCD.getText().trim(), hocPhi = txtHocPhi.getText().trim(), thoiLuong = txtThoiLuong.getText().trim(), moTa = txtMoTa.getText().trim();
        if (!Check.checkNull(ma, ten, hocPhi, thoiLuong)) {
            JOptionPane.showMessageDialog(this, "Nhập đủ dữ liệu", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (!Check.checkLength(1, 5, ma)) {
            JOptionPane.showMessageDialog(this, "Mã CD từ 1 - 5 ký tự", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (!Check.checkLength(1, 50, ten)) {
            JOptionPane.showMessageDialog(this, "Tên NV từ 1 - 50 ký tự", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (!Check.checkLength(0, 255, moTa)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu từ 0 - 255 ký tự", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (!Check.isANumberInteger(thoiLuong)) {
            JOptionPane.showMessageDialog(this, "Thời lượng là 1 số nguyên", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (!Check.isANumberDouble(hocPhi)) {
            JOptionPane.showMessageDialog(this, "Học phí là 1 số", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;

    }

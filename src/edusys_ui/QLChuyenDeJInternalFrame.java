    private void chonAnh() {
        JFileChooser jfc = new JFileChooser();
        jfc.setCurrentDirectory(new File(System.getProperty("user.dir") + "\\images")); // Lấy vị trị của project khi mở chooser
        FileFilter filter = new FileNameExtensionFilter("Image file", ImageIO.getReaderFileSuffixes());
        jfc.setFileFilter(filter);
        jfc.setAcceptAllFileFilterUsed(false); // Cho phép chọn các file ngoài filter 
        int chooser = jfc.showOpenDialog(this);
        if (chooser == JFileChooser.APPROVE_OPTION) {
            File f = jfc.getSelectedFile();
            System.out.println(f.toString());
            XImage.save(f);
            ImageIcon icon = XImage.read(f.getName());
            icon = scaleImage(icon);

            lblAnh.setText("");
            System.out.println(f.getAbsolutePath());
            lblAnh.setIcon(icon);
            lblAnh.setToolTipText(f.getName()); // Khi hover sẽ hiện tên của file được chọn
        } else {
            lblAnh.setIcon(null);
            lblAnh.setText("Chọn ảnh");
            lblAnh.setToolTipText(null);
        }
    }

    private ImageIcon scaleImage(ImageIcon icon) {
        if (icon.getImage().getWidth(null) > 189 && icon.getImage().getHeight(null) > 233) { // scale if width and height over allowed 
            Image image = icon.getImage().getScaledInstance(189, 233, Image.SCALE_SMOOTH);
            icon = new ImageIcon(image);
        }
        return icon;
    }


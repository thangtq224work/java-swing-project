    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (Auth.getRole()) {
            int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa chuyên đề " + txtMaCD.getText(), "Thông báo", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                index = tblBang.getSelectedRow();
                ChuyenDe cd = chuyenDeList.get(index);
                int success = daoCD.delete(txtMaCD.getText());
                if (success > 0) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    chuyenDeList.remove(index);
                    tableModel.removeRow(index);
                    if (tableModel.getRowCount() == 0) {
                        clearForm();
                    } else if (tableModel.getRowCount() == index) {
                        index--;
                        fillToForm(index);
                    } else {
                        fillToForm(index);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa không thành công", "Thông báo", JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn không có quyền xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnXoaActionPerformed

